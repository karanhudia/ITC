    private void loginbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbuttonActionPerformed
        String usr=ufield.getText();
        String pass=passfield.getText();
        try {
            Connection con=ConnectJDBC.getJDBC();
            Statement stmt=con.createStatement();
            String sql="Select * from loginid";
            int flag=-1;
            ResultSet rs=stmt.executeQuery(sql);
            while(rs.next())
            {
                String un=rs.getString(1);
                String ps=rs.getString(2);
                if(usr.equals(un)&&pass.equals(ps))
                {
                    flag=0;
                    addtoadmin();
                    this.hide();
                    new ITCGraphics().show();
                }
            }
            if(flag==-1)
                JOptionPane.showMessageDialog(null, "Wrong ID Password", "InfoBox: User Login", JOptionPane.INFORMATION_MESSAGE);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_loginbuttonActionPerformed

